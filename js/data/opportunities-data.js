// --- Computed Statistics ---
function computeOppStats() {
  const d = OPPORTUNITIES_DATA;
  const totalFunding = d.reduce((s, o) => s + (o.estimatedFunding || 0), 0);
  const bySponsor = {};
  const byCollege = {};
  const byType = {};
  const byStage = {};

  d.forEach(o => {
    bySponsor[o.sponsor] = (bySponsor[o.sponsor] || 0) + 1;
    byCollege[o.piCollege] = (byCollege[o.piCollege] || 0) + 1;
    byType[o.type] = (byType[o.type] || 0) + 1;
    byStage[o.stage] = (byStage[o.stage] || 0) + 1;
  });

  return { total: d.length, totalFunding, bySponsor, byCollege, byType, byStage };
}
